# CREATE A TRIANGLE PYRAMID .

# USING while LOOP .

# EXAMPLE ONE 

x = 1
while x <= 10 :
    print ( "*" * x )
    x = x + 1

# EXAMPLE TWO

y = 10
while y >= 1 :
    print ( "*" * y )
    y = y - 1

# CREATE A MULTIPLICATION TABLE OF 16 UP TO 20 NUMBERS .

z = 1     
while z <= 20 :
    print ( 16 , "*" , z , "=" , 16*z )
    z = z + 1